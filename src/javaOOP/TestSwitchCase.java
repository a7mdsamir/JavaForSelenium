package javaOOP;

public class TestSwitchCase {
    public static void main(String[] args) {

        int day = 4;
        switch (day){
            case 1 :
                System.out.println("Day 1");
                break;
            case 2 :
                System.out.println("Day 2");
                break;
            case 3 :
                System.out.println("Day 3");
                break;
            case 4 :
                System.out.println("Day 4");
                break;
            default :
                System.out.println("Invalid");

        }
    }
}